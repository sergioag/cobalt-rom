#ifndef EXTRA_TYPES_H
#define EXTRA_TYPES_H

#include <linux/types.h>

typedef unsigned char	uchar;
typedef __s8		int8;
typedef __u8		uint8;

typedef __s16		int16;
typedef __u16		uint16;

typedef __s32		int32;
typedef __u32		uint32;

typedef __s64		int64;
typedef __u64		uint64;

typedef uint8		byte;
typedef uint16		word;
typedef uint32		dword;
typedef uint64		qword;

#endif
